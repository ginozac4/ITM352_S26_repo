def load_csv(filepath): # defining a function to load a CSV file from a given filepath
    print(f"Loading data from {filepath}...") # telling the user that the file is being loaded
    start_time = time.time() # records start time
    try:
        df = pd.read_csv(filepath, engine='python')  
            # reads the csv file into a pandas DataFrame
        end_time = time.time() 
            #records end time
        load_time = end_time - start_time 
            # calculates the time taken to load the file
        print(f"CSV file loaded succesfully in {load_time:.2f} seconds.")
        print(f"number of rows: {len(df)}") 
            # prints the number of rows in the DataFrame
        print(f"Columns: {df.columns.tolist()}") 
        df['order_date'] = pd.to_datetime(df['order_date'], format='%m/%d/%Y', errors='coerce')  # Convert order_date to datetime so all the same format.
        df.fillna(0, inplace=True)  
            # Fill NaN values with 0 for numeric columns
        df['sales'] = df['quantity'] * df['unit_price']  
            # Create a new 'sales' column as quantity * unit_price

        required_columns = ['quantity', 'unit_price', 'order_date'] # defining a list of required columns that should be present in the DataFrame
            # Checks if required columns are present
        missing_columns = [col for col in required_columns if col not in df.columns]
        if missing_columns:
            print(f"Warning: Missing required columns: {missing_columns}. Some analytics may not work properly without these columns.")    
        else: 
            print("All required columns are present.")

        return df
    
    except Exception as e: 
        # if there is an error while loading the file, it will be caught here and an error message will be printed
        print(f"Error loading CSV file: {e}")
        return None

