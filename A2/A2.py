# INDIVIDUAL REQUIREMENT 8: Add an analytic that shows sales by region, product, showing the percentage of the quantity and total sales per region and product.
def show_sales_by_region_and_product_with_percentage(dataframe):
    if 'sales_region' not in dataframe.columns or 'product_category' not in dataframe.columns or 'sales' not in dataframe.columns or 'quantity' not in dataframe.columns:
        print("Error: Required columns for this analysis are missing. Please ensure 'sales_region', 'product_category', 'sales', and 'quantity' columns are present.")
        return

    pivot_table = pd.pivot_table(dataframe, values=['sales', 'quantity'], index='sales_region', columns='product_category', aggfunc='sum', fill_value=0)
    # the pivot table will show the sales with region as the index and product category as the columns, with missing values filled with 0
    pivot_table[('sales', 'Total')] = pivot_table['sales'].sum(axis=1)
    pivot_table[('quantity', 'Total')] = pivot_table['quantity'].sum(axis=1)
        # this code adds a new column to the pivot table called 'Total' under the sales and quantity groups.
        # it sums the sales and quantity across the category columns for the same region.
    for column in pivot_table['sales'].columns:
        pivot_table[('sales', column + ' % of Total')] = (
            (pivot_table[('sales', column)] / pivot_table[('sales', 'Total')]) * 100).round(2).astype(str) + '%'
    for column in pivot_table['quantity'].columns:
        pivot_table[('quantity', column + ' % of Total')] = (
            (pivot_table[('quantity', column)] / pivot_table[('quantity', 'Total')]) * 100).round(2).astype(str) + '%'
    # add the quantity percentage columns by iterating through each category column in the pivot table (except for the 'Total Sales' column) and calculating the percentage of total sales for each category, rounding to 2 decimal places, and adding a '%' sign to the end of the value.
    
    # this loop iterates through every value in the pivot table except for the 'Total Sales' column.
    # it divides the column by total sales to get the percentage.
    print("Sales by Region and Product Category, with Percentage of Total Sales:")
    print(pivot_table)
    return pivot_table

